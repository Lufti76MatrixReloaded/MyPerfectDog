data class DogsRepository( private val dataBase: DogsDataBase) {

	val dogsList: LiveData<List<DogsList>> = dataBase.dogsDataBaseDao.getAll()

	suspend fun getDogs() {
		withContext(Dispatchers.IO) {
			val newDogsList = this @DogsRepository.dataBase.().dogs
			dataBase.dogsDataBaseDao.insert(newDogsList)
		}
	}

	suspend fun insert (dogs: Dogs) {
		try {
			dataBase.dogsDataBaseDao.insert(dogs)
		} catch (e: Exception) {
			Log.d(TAG, "Failed to insert into Database: $e")
		}
	}

	suspend fun update(dogs: Dogs) {
		try {
			dataBase.dogsDataBaseDao.update(dogs)
		} catch (e: Exception) {
			Log.d(TAG, "Failed to update Database: $e")
		}
	}

	suspend fun delete(dogs: Dogs) {
		try {
			dataBase.dogsDataBaseDao.deleteById(dogs.id)
		} catch (e: Exception) {
			Log.d(TAG, "Failed to delete from Database: $e")
		}
	}
}

